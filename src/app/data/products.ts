// Helper functions
export const getProductById = (id: string): Product | undefined => {
  return products.find((p) => p.id === id);
};

export const getProductsByCategory = (category: string): Product[] => {
  if (category === "all") return products;
  return products.filter((p) => p.category === category);
};

export const getRecommendedProducts = (limit: number = 6): Product[] => {
  return products
    .filter((p) => p.demandLevel === "high" && p.distance <= 7)
    .slice(0, limit);
};

export const getNearbyProducts = (maxDistance: number = 5, limit: number = 6): Product[] => {
  return products
    .filter((p) => p.distance <= maxDistance)
    .sort((a, b) => a.distance - b.distance)
    .slice(0, limit);
};

export const getUrgentProducts = (limit: number = 4): Product[] => {
  return products.filter((p) => p.urgent).slice(0, limit);
};
